char	*ft_get_char(char *str)
{
	int		i;
	char	c[3];

	i = 0;
	while (str[i++] != '\n')
		;
	i--;
	c[2] = str[i];
	c[1] = str[i - 1];
	c[0] = str[i - 2];
	return (c);
}
